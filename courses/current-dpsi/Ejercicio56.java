public class Ejercicio56 {

    public static void main(String[] args) {
        int v = 7;
        int n = 1;
        int r = 0;
        while (n <= 10) {
            r = v * n;
            System.out.println("(" + v + "*" + n + ") = " + r);
            n++;

        }
    }

}
