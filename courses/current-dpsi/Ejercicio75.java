public class Ejercicio75 {

    public static void main(String[] args) {
        int suma = 0;
        System.out.println("Cual es la suma de todos los numeros de 1 al 100:");
        for (int n = 0; n <= 100; n++) {
            suma = suma + n;

        }
        System.out.println(suma);
    }
}
