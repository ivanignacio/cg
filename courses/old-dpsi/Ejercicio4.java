/*
    Definir una variable en Java es muy sencillo, basta indicarle el tipo de dato
    seguido del identificador de la variable.

    Ejemplo:    int edad;

    Nota que int es el tipo de dato y edad es el identificador de la variable. 
*/

public class Ejercicio4 {

    public static void main(String[] args) {
       
        int edad;   //Definición de la variable edad de tipo int
        edad = 23;  //Asignación del valor 23 a la variable edad
        
        //Impresión de la variable edad a través de la consola.
        System.out.println("Mi edad es " +edad+" años.");
    }
}
